/** Намерение кнопки, влияющее на отображение в клиентах Max. */
export type ButtonIntent = 'default' | 'positive' | 'negative';

/** После нажатия на такую кнопку клиент отправляет на сервер полезную нагрузку. */
export type CallbackButton = {
  type: 'callback';
  /**
   * Видимый текст кнопки. Чтобы он отображался полностью, рекомендуем не превышать:
   * `20` символов при 1 кнопке в ряду, `10` при 2, `5` при 3, `3` при 4.
   *
   * Ограничение API: от `1` до `128` символов.
   */
  text: string;
  /** Токен кнопки. Ограничение API: до `1024` символов. */
  payload: string;
  /** @deprecated Отключено на клиенте */
  intent?: ButtonIntent;
};

/** После нажатия на такую кнопку пользователь переходит по ссылке, которую она содержит. */
export type LinkButton = {
  type: 'link';
  /**
   * Видимый текст кнопки. Чтобы он отображался полностью, рекомендуем не превышать:
   * `20` символов при 1 кнопке в ряду, `10` при 2, `5` при 3, `3` при 4.
   *
   * Ограничение API: от `1` до `128` символов.
   */
  text: string;
  /** Ссылка для открытия. Ограничение API: до `2048` символов. */
  url: string;
};

/** После нажатия клиент отправляет новое сообщение с вложением текущего контакта пользователя. */
export type RequestContactButton = {
  type: 'request_contact';
  /** Видимый текст кнопки. Ограничение API: от `1` до `128` символов. */
  text: string;
};

/** После нажатия клиент отправляет новое сообщение с текущим географическим положением пользователя. */
export type RequestGeoLocationButton = {
  type: 'request_geo_location';
  /** Видимый текст кнопки. Ограничение API: от `1` до `128` символов. */
  text: string;
  /** Если `true`, отправляет местоположение без запроса подтверждения пользователя. */
  quick?: boolean;
};

/** Кнопка запуска мини-приложения. */
export type OpenAppButton = {
  type: 'open_app';
  /** Видимый текст кнопки. Ограничение API: от `1` до `128` символов. */
  text: string;
  /** Публичное имя (`username`) бота или ссылка на него, чьё мини-приложение надо запустить. */
  web_app?: string | null;
  /** Параметр запуска, который будет передан в `initData` мини-приложения. */
  payload?: string | null;
  /** Идентификатор бота, чьё мини-приложение надо запустить. */
  contact_id?: number | null;
};

/** Кнопка, после нажатия на которую клиент отправляет текст кнопки в чат от лица пользователя. */
export type MessageButton = {
  type: 'message';
  /** Текст кнопки, который будет отправлен в чат от лица пользователя. Ограничение API: от `1` до `128` символов. */
  text: string;
};

/** После нажатия на кнопку указанный текст копируется в буфер обмена. */
export type ClipboardButton = {
  type: 'clipboard';
  /** Видимый текст кнопки. Ограничение API: от `1` до `128` символов. */
  text: string;
  /** Текст, который копируется в буфер обмена после нажатия на кнопку. */
  payload: string;
};

/**
 * @deprecated Серверный API больше не поддерживает кнопку `chat`.
 * Оставлено только для совместимости со старым кодом.
 */
export type ChatButton = {
  type: 'chat';
  /** Видимый текст кнопки. */
  text: string;
  /** Название создаваемого чата. */
  chat_title: string;
  /** Описание создаваемого чата. */
  chat_description?: string | null;
  /** Payload запуска создаваемого чата. */
  start_payload?: string | null;
  /** Legacy UUID кнопки. */
  uuid?: string | null;
};

/** Поддерживаемые актуальным API типы кнопок для inline keyboard. */
export type Button =
  | CallbackButton
  | LinkButton
  | RequestContactButton
  | RequestGeoLocationButton
  | OpenAppButton
  | MessageButton
  | ClipboardButton;
